def scores(word):
    sums = (0, 0, 0, 0, 0, 0)

    for kar in word:
        if kar not in _scores:
            logging.warn('UNKNOWN KAR %s!' % kar)
            continue
        sums = tuple(sum(t) for t in zip(sums, _scores[kar]))

    return ['scrabble\nscore: %i' % sums[0],
            'vowel ratio:\n%i:%i' % (sums[1], sums[2])
            ]
